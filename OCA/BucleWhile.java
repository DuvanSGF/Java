package calculadora;

public class BucleWhile {

	public static void main(String[] args) {
		
		/*
		 * Este Ejercico lo colocaron en una prueba:
		 * Cual es el valor de la varible i al final
		 * del codigo: La resolvi bn*/
		int n = 0;
		int i = 1;
		
		while(i < n) {
			i = i + 1;
			System.out.println("El valor de i es :" + i);
			break;
			
		}
		System.out.println("Ciclo terminado");

	}

}
